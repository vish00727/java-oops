package OOps;

public class ThreeDArray {
    public static void main(String[] args) {
        int [][][] arr= new int[2][3][3];


        for (int i=0; i<2; i++){
            for (int j=0; j<3; j++){
                for (int k=0; k<3; k++){
                    System.out.print(i);
                }
                System.out.println(" ");
            }
            System.out.println(" ");
        }
        System.out.println(" ");
    }
}
